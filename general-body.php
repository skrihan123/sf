      <h3>General Provisions</h3>
      <p></p>
      <p>A General Body meeting may be classified into two categories: an Ordinary General Body Meeting or a Special General Body Meeting.</p>

      <h3>Ordinary General Body Meeting (AGM)</h3>
      <p>The Ordinary General Body Meeting shall be held annually. The Executive Committee will determine the place and date for the meeting. All Member Associations shall be notified in writing at least 30 (thirty) days in advance. In the case of an Ordinary Annual General Body Meeting (AGM) where elections are conducted, the nomination forms and electoral college list shall be included with the notification, also sent at least 30 (thirty) days prior to the meeting. A formal convocation shall be issued in writing at least 15 (fifteen) days before the date of the Ordinary General Body Meeting. The convocation will include the agenda, financial statements, and the Auditor's report.</p>
      <p></p>

      <h3>Special General Body Meeting</h3>
      <p>The Executive Committee has the discretion to convene a Special General Body Meeting at any time.</p>
      <p></p>

      <h3>Special General Body Meeting on Request</h3>
      <p>If one-third of the Member Associations request in writing for a Special General Body Meeting, specifying the items to be included in the agenda, the Executive Committee shall convene such a meeting. A Special General Body Meeting shall be held within 60 (sixty) days of receiving the request.</p>
      <p></p>

      <h3>Notification of Special General Body Meeting</h3>
      <p>Members shall be notified of the place, date, and agenda of a Special General Body Meeting at least 30 (thirty) days in advance. The agenda for a Special General Body Meeting cannot be altered once it has been communicated.</p>
      <p></p>

      <h3>Attendance of Honorary President and Others</h3>
      <p>The 'Honorary President,' as well as individuals specially designated by the General Body, along with the members of the Executive Committee and the General Secretary of the Iconic Sports Federation, will attend the General Body Meetings in a consultative capacity.</p>
      <p></p>

      <h3>Invitees and Third-Party Attendance</h3>
      <P>The Executive Committee may invite third parties to attend the General Body Meetings. Third parties do not have voting rights and may only be consulted with the permission of the Executive Committee.</P>
      <P></P>

      <h3>Timing of the Annual General Body Meeting</h3>
      <p>The Annual General Body Meeting shall be convened within nine months following the end of the financial year of the Iconic Sports Federation. However, in special circumstances, the Executive Committee may extend this period by a maximum of three months.</p>
      <p></p>

      <h3>Issuance of Meeting Notices</h3>
      <p>Notices for all meetings will be issued by the General Secretariat.</p>
      <p></p>

      <h3>Quorum for Meetings</h3>
      <p>The quorum for both the Annual or Special General Body Meetings shall be 50% of the Members plus one.</p>
      <p></p>

      <h3>Voting Rights of Permanent Members</h3>
      <p>Each Permanent Member of the Iconic Sports Federation (Permanent Member refers to a Football Association of a State or Union Territory as specified in the Constitution of India, which has been granted permanent affiliation to the Federation) shall have one vote in the General Body Meeting. Voting will be conducted through delegates, and voting by proxy or by letter is strictly prohibited.</p>
      <p></p>

      <h3>Delegate Representation</h3>
      <p>Delegates representing a Permanent Member must be affiliated with that Member and must be nominated by the appropriate body of the respective Permanent Member. Nominations must be signed by the President or the Honorary/General Secretary of the Permanent Member. In cases of conflict, the nomination signed by the President shall be deemed valid. Additionally, no individual who has been criminally convicted by a court of law can represent a Permanent Member as a delegate.</p>
      <p></p>

      <h3>Executive Committee Members and Voting Rights</h3>
      <p>A member of the Executive Committee of the Iconic Sports Federation shall not have voting rights during the Annual and Special General Body Meetings. They are also prohibited from being nominated as a representative of the respective Member Association during their tenure as a member of the Executive Committee.</p>
      <p></p>

      <h3>Conduct of General Body Meetings</h3>
      <p>The President of the Iconic Sports Federation shall oversee and conduct the business of the General Body Meeting in strict compliance with the Standing Orders of the General Body.</p>
